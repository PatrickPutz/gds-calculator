public class Calculator {
    public static void main(String[] args) {
        int a = 27;
        System.out.println("a = " + a);
        double b = 17.5;
        System.out.println("b = " + b);
        boolean c = false;
        System.out.println("c = " + c);
        char d = 'Y';
        System.out.println("d = " + d);
        String e = "Bamboozled";
        System.out.println("e = " + e);
    }
}
